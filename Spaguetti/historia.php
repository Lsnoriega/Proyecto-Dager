<h3><i class="bi-calendar me-2"> 2001</i></h3>
<h5 class="fw-bold">Creación</h5>
<p align="justify" class="mb-5 mt-1">
<i class="mx-3"></i>
El Instituto Universitario de Tecnología del Estado Bolívar (Iuteb) fue creado el 26 de Enero de 2001, mediante Decreto N.1186, del Presidente Hugo Chávez Frías, en el marco de la política de inclusión y justicia social del Gobierno Bolivariano. Este primer tecnológico universitario oficial para estado fue la respuesta a la necesidad de contar con una institución que respondiera a la elevada población excluida y creciente demanda estudiantil de la región producto de las políticas privatizadoras que se habían instaurado en el país. El Iuteb y los tecnológicos de Barinas y Apure fueron las tres primeras instituciones de educación universitaria creadas en la gestión del Gobierno Bolivariano.

El 27 de noviembre del 2001 tuvo lugar el acto oficial de inicio de actividades que incluyó un curso de actualización de conocimientos, con el propósito de explorar habilidades, destrezas y conocimientos esenciales para cumplir con los requisitos de una carrera a nivel universitario. El 01 de abril del 2002, con 487 alumnos, se dio inicio al primer semestre académico con cuatro especialidades: Electricidad, Geología y Minas, Mecánica y Sistemas Industriales que respondieron a las exigencias del desarrollo local, regional y nacional, propuestas en función de la oferta y demanda del mercado laboral; la oferta académica de otras instituciones de educación en la región y los planes de desarrollo local, regional y nacional tanto de la Corporación Venezolana de Guayana como de la Gobernación del Estado Bolívar.
</p>

<h3><i class="bi-calendar me-2"> 2005</i></h3>
<h5 class="fw-bold">Promociones</h5>
<p align="justify" class="mb-5">
<i class="mx-3"></i> 

La primera promoción de TSU egresó el 3 de febrero de 2005, conformada por 130 nuevos profesionales de la Patria, promoción que fue honrada con el nombre epónimo “Congreso de Angostura”. La primera promoción de TSU e ingenieros formados en lo Programas Nacionales de Formación (PNF) egresó el 01 de diciembre de 2011, con el nombre epónimo “Firma del Acta de Independencia de Venezuela”

Los Programas Nacionales de Formación comenzaron a gestionarse a partir de 2008, en el marco del proceso de transformación impulsado por el entonces Ministerio del Poder Popular para la Educación Universitaria (Mppeu), que en la actualidad son siete: Electricidad, Geociencia, Informática, Joyería y Orfebrería, Mantenimiento, Mecánica, y Sistemas de Calidad y Ambiente. Hasta diciembre de 2017 han egresado del Iuteb un total de 6.502 profesionales (2.054 ing. y 4.448 TSU) cosecha bolivariana entregada al país en 22 promociones de TSU-Iuteb-Carrera y en 11 promociones de los PNF.
</p>

