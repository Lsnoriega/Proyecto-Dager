<p align="justify" class="mb-5">
<i class="mx-3"></i>
Universidad Politécnica Territorial del estado Bolívar creada por el Presidente Nicolás Maduro Moros, mediante Decreto Nº 3683, publicado en Gaceta Oficial Nº 41531, de fecha 23/11/18. Esta nueva casa de estudios nace de la transformación del Instituto Universitario de Tecnología del Estado Bolívar (IUTEB).
</p>

