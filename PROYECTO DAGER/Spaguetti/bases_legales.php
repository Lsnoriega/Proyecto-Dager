  <h4 id="list-item-1">Constitución de la República Bolivariana de Venezuela (1999)</h4>
  <p align="justify">
  	<strong>Artículo 108.</strong>  Los medios de comunicación social, públicos y privados, deben contribuir a la formación ciudadana. El Estado garantizará servicios públicos de radio, televisión y redes de bibliotecas y de informática, con el fin de permitir el acceso universal a la información. Los centros educativos deben incorporar el conocimiento y aplicación de las nuevas tecnologías, de sus innovaciones, según los requisitos que establezca la ley.

  	<br><br><strong>Artículo 110.</strong>  El Estado reconocerá el interés público de la ciencia, la tecnología, el conocimiento, la innovación y sus aplicaciones y los servicios de información necesarios por ser instrumentos fundamentales para el desarrollo económico, social y político del país, así como para la seguridad y soberanía nacional. Para el fomento y desarrollo de esas actividades, el Estado destinará recursos suficientes y creará el sistema nacional de ciencia y tecnología de acuerdo con la ley. El sector privado deberá aportar recursos para las mismas. El Estado garantizará el cumplimiento de los principios éticos y legales que deben regir las actividades de investigación científica, humanística y tecnológica. La ley determinará los modos y medios para dar cumplimiento a esta garantía.
</p>
  
  <br><h4 id="list-item-2">Ley Orgánica De Ciencia, Tecnología e Innovación (2010)</h4>
  <p align="justify"><strong>Artículo 1.</strong>  La presente Ley tiene por objeto desarrollar los principios orientadores que en materia de ciencia, tecnología e innovación y sus aplicaciones, establece la Constitución de la República Bolivariana de Venezuela, organizar el Sistema Nacional de Ciencia, Tecnología e Innovación, definir los lineamientos que orientarán las políticas y estrategias para la actividad científica,  tecnológica, de innovación y sus aplicaciones, con la implantación de mecanismos institucionales y operativos  para  la promoción, estímulo y fomento de la investigación científica, la apropiación social del conocimiento y la transferencia e innovación tecnológica, a fin de fomentar la capacidad para la generación, uso y circulación del conocimiento y de impulsar el desarrollo nacional.

<br><br><strong>Artículo 2.</strong>  Las actividades científicas, tecnológicas, de innovación y sus aplicaciones son de interés público y de interés general. 
 
<br><br><strong>Artículo 3.</strong>  Forman parte del Sistema Nacional de Ciencia Tecnología e Innovación, las instituciones públicas o privadas que generen y desarrollen conocimientos científicos y tecnológicos, como procesos de innovación, y las personas que se dediquen a la planificación, administración, ejecución y aplicación de actividades que posibiliten la vinculación efectiva entre la ciencia, la tecnología y la sociedad. A tal efecto, los sujetos que forman parte del Sistema son: 
 
<br><br><strong>1.</strong>  El Ministerio de Ciencia y Tecnología, sus organismos adscritos y las entidades tuteladas por éstos, o aquéllas en las que tengan participación. 
 
<br><br><strong>2.</strong>  Las instituciones de educación superior y de formación técnica, academias nacionales, colegios profesionales, sociedades científicas, laboratorios y centros de investigación y desarrollos, tanto públicos como privados.

<br><br><strong>3.</strong> Los organismos del sector privado, empresas, proveedores de servicios, insumos y bienes de capital, redes de información y asistencia que sean incorporados al Sistema. 
 
<br><br><strong>4.</strong> Las unidades de investigación y desarrollo, así como las unidades de tecnologías de información y comunicación de todos los organismos públicos. 
 
<br><br><strong>5.</strong> Las personas públicas o privadas que realicen actividades de ciencia, tecnología, innovación y sus aplicaciones.
</p>
  

  <br><h4 id="list-item-3">Ley De Infogobierno (2014)</h4>
  <p align="justify">

  	<strong>Artículo 1.</strong>  Esta ley tiene por objeto establecer las normas, principios y lineamientos aplicables a las tecnologías de información que generen y utilicen los sujetos a que se refiere el artículo 4 de esta ley, con el fin de mejorar la gestión pública y hacerla transparente, facilitar el acceso de los ciudadanos a la información en sus roles de contralor y usuario, además de promover el desarrollo nacional que garantice la soberanía tecnológica. 

  	<br><br><strong>Artículo 5.</strong>  Las actividades inherentes al Sector de Tecnologías de Información del Estado son declaradas de interés público y de carácter prioritario, estratégico y de seguridad y defensa para el desarrollo nacional.
</p>
  

  <br><h4 id="list-item-4">Decreto N° 825</h4>
  <p align="justify">Decreto que normaliza el acceso y uso de Internet como política prioritaria para el desarrollo cultural, económico, social y político de la República Bolivariana de Venezuela (Gaceta N° 36.955, de fecha 22/05/2000).

<br><br><strong>Artículo 1.</strong>  Se declara el acceso y el uso de Internet como política prioritaria para el desarrollo cultural, económico, social y político de la República Bolivariana de Venezuela.

<br><br><strong>Artículo 5.</strong>  El Ministerio de Educación, Cultura y Deportes dictará las directrices tendentes a instruir sobre el uso de Internet, el comercio electrónico, la interrelación y la sociedad del conocimiento. Para la correcta implementación de lo indicado, deberán incluirse estos temas en los planes de mejoramiento profesional del magisterio.

<br><br><strong>Artículo 8.</strong>  En un plazo no mayor de tres (3) años, el cincuenta por ciento (50%) de los programas educativos de educación básica y diversificada deberán estar disponibles en formatos de Internet, de manera tal que permitan el aprovechamiento de las facilidades interactivas, todo ello previa coordinación del Ministerio de Educación, Cultura y Deportes.
</p>
  

  <br><h4 id="list-item-5">Decreto N° 3.390</h4>
  <p align="justify">Este Decreto de fecha 23 de diciembre de 2004, publicado en Gaceta Oficial N° 38.095 el 28 de diciembre del mismo año.

<br><br><strong>Artículo 1.</strong>  La Administración Pública Nacional empleará prioritariamente Software libre desarrollado con Estándares Abiertos, en sus sistemas, proyectos y servicios informáticos. A tales fines, todos los órganos y entes de la Administración Pública Nacional iniciarán los procesos de migración gradual y progresiva de éstos hacia el Software Libre desarrollado con Estándares Abiertos.

<br><br><strong>Artículo 10.</strong>  El Ministerio de Educación y Deportes, en coordinación con el Ministerio de Ciencia y Tecnología, establecerá las políticas para incluir el Software Libre desarrollado con Estándares Abiertos, en los programas de educación básica y diversificada.
</p>
  

  <br><h4 id="list-item-6">Decreto Nº 3.683</h4>
  <p align="justify"> 
  	<strong>Artículo 1.</strong>   Se crea la Universidad Politécnica Territorial del Estado Bolívar, en el marco de la Misión Alma Mater, como universidad experimental, con    personalidad jurídica y patrimonio propio, distinto e independiente del Tesoro Nacional.</p>


  <br><h4 id="list-item-7">Vinculación con el Plan de la Patria 2019 - 2025</h4>
  <p align="justify">
  	<strong>Objetivo Histórico I.</strong>  Defender, expandir y consolidar el bien más preciado que hemos reconquistado después de 200 años:  la Independencia Nacional.

<br><br><strong>Objetivo Nacional</strong> 

<br><br><strong>1.6.</strong>  Desarrollar las capacidades científico-tecnológicas que hagan viable, potencien y blinden la protección y atención de las necesidades del pueblo y el desarrollo del país potencia.

<br><br><strong>1.6.1.</strong>  Consolidar un estilo científico, tecnológico e innovador de carácter transformador, diverso, creativo y dinámico, garante de la independencia y la soberanía económica, contribuyendo así a la construcción del modelo productivo socialista, el fortalecimiento de la ética socialista y la satisfacción efectiva de las necesidades del pueblo venezolano.

<br><br><strong>1.6.1.1.</strong>  Desarrollar una actividad científica, tecnológica y de innovación, transdisciplinaria, asociada directamente a la estructura productiva nacional, sustitución de importaciones en nudos críticos, así como fomentar el desarrollo de procesos de escalamiento industrial orientados al aprovechamiento de las potencialidades, con efectiva transferencia de conocimientos para la soberanía tecnológica.

<br><br><strong>1.6.1.4.</strong>  Democratizar el acceso físico, cultural y económico a las tecnologías de información y comunicación, que contemplen los procesos formativos, adecuación de la infraestructura y equipamiento, en particular al sistema educativo.

<br><br><strong>1.6.2.</strong>  Fortalecer los espacios y programas de formación para el trabajo liberador, fomentando los valores patrióticos, la descolonización y el sentido crítico.

<br><br><strong>1.6.2.5.</strong>  Desarrollar aplicaciones informáticas que permitan aumentar la eficacia en la atención de las necesidades sociales.

<br><br><strong>1.6.2.6.</strong>  Obtener el máximo provecho de las tecnologías de información y comunicación (TIC) para la difusión de contenidos, basados en valores nacionales, multiétnicos y pluriculturales de nuestros pueblos y, con ellos, los principios inherentes al Socialismo Bolivariano.
.
<br><br><strong>1.6.3.</strong>   Impulsar el desarrollo y uso de equipos electrónicos y aplicaciones informáticas, basados en tecnologías libres y estándares abiertos.

<br><br><strong>1.6.3.1.</strong>  Garantizar el impulso de la formación y transferencia de conocimiento, que permita el desarrollo de equipos electrónicos y aplicaciones informáticas en tecnologías libres y estándares abiertos.

<br><br><strong>1.6.3.1.3.</strong>  Desarrollar aplicaciones informáticas que atiendan necesidades sociales.
</p>

